# Python is an implicit typed language as we don't need to declare the data types

print("Hello")
print("My name is Harsh Kumar Mahto","I am a CSE student")  #Both the parts will be printed in the same line
print(25*4)

# variables are not required to be declared with data type
name="Harsh"
rollNo=94
print(name)
print(rollNo)

# type is used to get the data type of a variable
# data types are int,float,str,bool,none
print(type(name))
print(type(rollNo))
a=None
print(type(a))
adult=False
print(type(adult))